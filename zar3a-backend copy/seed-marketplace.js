async function seed() {
  try {
    await sequelize.authenticate();
    console.log('✅ Connected to database.\n');

    // Clear existing seeded products to avoid duplicates
    await Product.destroy({ where: {} });
    console.log('🗑  Cleared existing products.\n');

    // Insert crop products
    const cropInserted = await Product.bulkCreate(cropProducts);
    console.log(`🌾 Inserted ${cropInserted.length} Crop Market products.`);

    // Insert agri products
    const agriInserted = await Product.bulkCreate(agriProducts);
    console.log(`🛒 Inserted ${agriInserted.length} Agri Shop products.`);

    console.log(`\n✅ Seeding complete! Total: ${cropInserted.length + agriInserted.length} products.`);
  } catch (err) {
    console.error('❌ Seeding failed:', err.message);
    console.error(err);
  } finally {
    await sequelize.close();
  }
}

